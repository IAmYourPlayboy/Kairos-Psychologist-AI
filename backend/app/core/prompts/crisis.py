"""Кризисные промпт-дополнения для трёх уровней угрозы.

Принципы (Сессия 27, см. CLAUDE.md «Принцип без хардкодных фраз»
и «Двухуровневая система запретов»):

- В промпте НЕТ примеров фраз бота. LLM формулирует каждый ответ сама.
- В промпте есть: ПРОТОКОЛ (что делать), ЗАПРЕТЫ (что НЕ говорить),
  ДАННЫЕ (конкретные телефоны для возрастной группы), ФОРМАТ ВЫВОДА.
- Кризисные телефоны инжектятся через `format_contacts_for_prompt(age_group)`
  из `core/crisis/contacts.py` — единый источник правды для всех промптов.

До Сессии 27 промпт содержал буквальные фразы ответа бота
(«Я слышу тебя. Прямо сейчас позвони: 📞 ...»). LLM их копировала как
шаблон, пользователь видел одинаковые ответы, телефон 8-800-2000-122
(детский) попадал в ответы для взрослых независимо от `age_group`.
Переписано под новый принцип.
"""

from __future__ import annotations

from typing import Literal

from app.core.crisis.contacts import format_contacts_for_prompt


CrisisLevel = Literal["normal", "elevated", "high", "immediate"]

# Уровни кризиса (в порядке возрастания тяжести).
LEVELS: tuple[CrisisLevel, ...] = ("normal", "elevated", "high", "immediate")


# ============================================================================
# ПРОТОКОЛЫ — что делать на каждом уровне.
#
# Описывают поведение, НЕ содержат примеров фраз. LLM формулирует сама.
# ============================================================================


_PROTOCOL_ELEVATED = """## КРИЗИСНЫЙ РЕЖИМ: ПОВЫШЕННЫЙ (elevated)
Обнаружены признаки сильного дистресса. Протокол:

- Усиль эмпатию. Валидируй состояние пользователя (признай его чувства), прежде чем предлагать что-либо.
- Сократи сообщения до 2-3 предложений.
- Не задавай открытых вопросов («что ты чувствуешь?»). Только выбор из 2 вариантов или да/нет.
- Мониторь каждое следующее сообщение на ухудшение состояния.
- Если видишь признаки ухудшения — переходи на уровень high (см. протокол high).
"""


_PROTOCOL_HIGH = """## КРИЗИСНЫЙ РЕЖИМ: ВЫСОКИЙ (high)
Обнаружены признаки безысходности или сильного отчаяния. Протокол:

- Сначала валидируй состояние в виде уточняющего вопроса (признай тяжесть, спроси подтверждение).
- Дождись подтверждения пользователя прежде чем переходить к действию.
- После подтверждения предложи кризисные контакты (см. блок КРИЗИСНЫЕ КОНТАКТЫ ниже).
- Не давай советов, не анализируй причины. Присутствие важнее объяснений.
- Максимум 2-3 предложения на сообщение.
"""


_PROTOCOL_IMMEDIATE = """## КРИЗИСНЫЙ РЕЖИМ: НЕМЕДЛЕННЫЙ (immediate)
Обнаружены суицидальные маркеры. Жизнь пользователя может быть в опасности. Протокол:

- НЕ задавай вопросов. НЕ валидируй длинно. Минимум слов ДО контактов.
- Сразу дай кризисные контакты (см. блок КРИЗИСНЫЕ КОНТАКТЫ ниже).
- После контактов — оставайся на связи короткими фразами присутствия.
- Обращайся на «ты».
- Никаких эмодзи.
"""


# ============================================================================
# ЗАПРЕТЫ — что бот НИКОГДА не говорит в кризисе.
#
# Это защитная граница, НЕ шаблон. LLM формулирует свои слова, но эти —
# никогда. Уровень 1 двухуровневой системы (см. CLAUDE.md).
#
# Ситуативные запреты (Уровень 2) подмешиваются позже через digest-ы
# конкретных тем — например, при утрате отдельно запрещено «время лечит».
# ============================================================================


_FORBIDDEN_GLOBAL_IN_CRISIS = """## ЗАПРЕЩЁННЫЕ ФРАЗЫ В КРИЗИСЕ (говорить НИКОГДА)

- «Я понимаю, что ты чувствуешь» — бот не чувствует, это ложная эмпатия.
- «Всё будет хорошо» — обещание будущего, обесценивает настоящее.
- «Держись» — пустое слово, не помогает.
- «Тебе нужно успокоиться» — давление, усиливает тревогу.
- «Бывает и хуже» — минимизация, обесценивание.
- «Ты сильный / сильная» — навязывание ожидания, ребёнку особенно вредит.
- «Что ты чувствуешь?» в остром кризисе — усиливает затопление эмоциями.
- «Не переживай» — отрицание чувств.
- «Время лечит» — не всегда лечит, обесценивает боль.
- «Всё наладится» — ложное обещание.
- Обсуждение методов (суицида, самоповреждения) в immediate — никогда.
- Обещания что «станет лучше» — никогда. Можно только «я здесь».
"""


# ============================================================================
# ФОРМАТ ВЫВОДА — технические правила представления текста.
# ============================================================================


_OUTPUT_FORMAT = """## ФОРМАТ ВЫВОДА

- Никаких эмодзи в кризисных ответах. Только текст.
- Каждый телефон из блока КРИЗИСНЫЕ КОНТАКТЫ — на отдельной строке (перенос `\\n`).
- Между абзацами текста — пустая строка (двойной перенос).
- В immediate: максимум 4-5 строк всего. Минимум слов.
- В high: максимум 4-5 строк.
- В elevated: максимум 3-4 строки.
"""


# ============================================================================
# СБОРКА: функция `build_crisis_prompt` — единственная точка входа.
# ============================================================================


_PROTOCOLS: dict[str, str] = {
    "elevated": _PROTOCOL_ELEVATED,
    "high": _PROTOCOL_HIGH,
    "immediate": _PROTOCOL_IMMEDIATE,
}


def build_crisis_prompt(level: str, age_group: str | None = None) -> str | None:
    """Собрать кризисный промпт для данного уровня и возрастной группы.

    Возвращает None для `level="normal"` — на нормальном уровне
    кризисный блок в промпт не добавляется.

    Структура итогового текста:
        1. Протокол (что делать) — из `_PROTOCOLS[level]`
        2. Запреты (что НЕ говорить) — общий блок `_FORBIDDEN_GLOBAL_IN_CRISIS`
        3. Данные (кризисные контакты для возрастной группы) —
           из `format_contacts_for_prompt(age_group)`
        4. Формат вывода (эмодзи/переносы) — общий блок `_OUTPUT_FORMAT`

    Args:
        level: "normal" / "elevated" / "high" / "immediate".
        age_group: "child" / "youth" / "adult" / None — определяет какие
            кризисные контакты попадут в блок данных. По умолчанию None —
            тогда показываем все контакты (консервативный дефолт).

    Returns:
        Текст блока для вставки в системный промпт, либо None если
        уровень "normal" (в normal кризисный блок не нужен).

    Example:
        >>> prompt = build_crisis_prompt("high", age_group="adult")
        >>> "8-800-2000-122" in prompt  # детский телефон
        False
        >>> "8-800-700-84-60" in prompt  # Линия 0-24 для взрослых
        True
    """
    if level == "normal":
        return None

    protocol = _PROTOCOLS.get(level)
    if protocol is None:
        # Неизвестный уровень — не рискуем, не добавляем кризисный блок.
        return None

    contacts_block = format_contacts_for_prompt(age_group)
    data_block = (
        "## КРИЗИСНЫЕ КОНТАКТЫ (используй ТОЛЬКО эти номера, больше никаких)\n\n"
        f"{contacts_block}"
    )

    return "\n\n".join([
        protocol,
        _FORBIDDEN_GLOBAL_IN_CRISIS,
        data_block,
        _OUTPUT_FORMAT,
    ])
