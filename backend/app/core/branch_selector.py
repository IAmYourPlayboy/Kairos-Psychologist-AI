"""Выбор терапевтической ветки (A — мобилизация / B — стабилизация).

Согласно CLAUDE.md (СТРУКТУРА ПРОЕКТА → branch_a / branch_b):
- Ветка A: глаголы действия в речи пользователя («надо», «бежать», «что делать»)
  → бот-«Штаб», SIX C's
- Ветка B: описания состояний («страшно», «трясёт», «не могу»)
  → бот-«Инструктор», ВОЗ PFA

Это **rule-based** реализация для MVP. В Блоке 12 (Aniemore) она будет заменена
на двухслойный NLP-пайплайн с эмоциями + темами.

Принцип fallback: если непонятно — ставим B (стабилизация). Это безопаснее,
потому что стабилизация подходит шире (даже если человек хочет действовать,
ему сначала надо стабилизироваться).
"""

import re

# Маркеры действия → ветка A (Мобилизация / SIX C's)
_ACTION_MARKERS = {
    # Глаголы намерения
    "надо",
    "нужно",
    "должен",
    "должна",
    "хочу",
    "буду",
    "сделать",
    "решить",
    "понять",
    "разобраться",
    "разобрать",
    "сделать выбор",
    # Вопросительные слова действия
    "что делать",
    "как быть",
    "куда идти",
    "куда обратиться",
    "что предпринять",
    "с чего начать",
    "как поступить",
    "как мне",
    # Слова срочности
    "срочно",
    "немедленно",
    "прямо сейчас",
    # Глаголы движения
    "бежать",
    "уходить",
    "уйти",
    "приехать",
    "приехать в",
    "ехать",
}

# Маркеры состояния → ветка B (Стабилизация / ВОЗ PFA)
_STATE_MARKERS = {
    # Физические ощущения
    "страшно",
    "ужасно",
    "трясёт",
    "трясет",
    "колотит",
    "тошнит",
    "задыхаюсь",
    "не могу дышать",
    "сердце колотится",
    "в груди",
    "в голове",
    "ноги ватные",
    "руки трясутся",
    # Состояния
    "не могу",
    "не справляюсь",
    "не получается",
    "паника",
    "ужас",
    "отчаяние",
    "оцепенение",
    "пустота",
    "мне плохо",
    "мне очень плохо",
    "плохо",
    # Эмоциональные состояния
    "плачу",
    "слёзы",
    "слезы",
    "не могу остановиться",
    "потерял контроль",
    "схожу с ума",
    "теряю себя",
}


def _normalize(text: str) -> str:
    """Привести к нижнему регистру и убрать лишние пробелы."""
    text = text.lower().strip()
    return re.sub(r"\s+", " ", text)


def select_branch(message: str) -> str:
    """Выбрать ветку терапевтического протокола.

    Args:
        message: Текст сообщения пользователя.

    Returns:
        "A" — мобилизация (глаголы действия преобладают)
        "B" — стабилизация (описания состояний или непонятно — fallback)
    """
    normalized = _normalize(message)

    action_count = sum(1 for marker in _ACTION_MARKERS if marker in normalized)
    state_count = sum(1 for marker in _STATE_MARKERS if marker in normalized)

    # Чёткое преобладание действий → A
    if action_count > state_count and action_count >= 1:
        return "A"

    # Иначе — B (fallback: стабилизация безопаснее)
    return "B"


def get_branch_stats(message: str) -> dict:
    """Получить детали выбора ветки (для отладки и логов)."""
    normalized = _normalize(message)

    matched_actions = [m for m in _ACTION_MARKERS if m in normalized]
    matched_states = [m for m in _STATE_MARKERS if m in normalized]

    branch = select_branch(message)
    return {
        "branch": branch,
        "action_count": len(matched_actions),
        "state_count": len(matched_states),
        "matched_actions": matched_actions,
        "matched_states": matched_states,
    }
