# ============================================================================

def get_pfa_technique(name: str) -> PFATechnique:
    """
    Получить технику PFA по имени.

    Args:
        name: Имя техники

    Returns:
        PFATechnique: Техника PFA

    Example:
        >>> technique = get_pfa_technique("5_4_3_2_1")
        >>> print(technique.steps)
    """
    return ALL_PFA_TECHNIQUES.get(name)


def get_pfa_by_category(category: str) -> Dict[str, PFATechnique]:
    """
    Получить все техники PFA по категории.

    Args:
        category: Категория (look, listen, link, grounding)

    Returns:
        Dict[str, PFATechnique]: Словарь техник

    Example:
        >>> grounding = get_pfa_by_category("grounding")
        >>> print(list(grounding.keys()))
        ['5_4_3_2_1', 'box_breathing', 'physical_grounding']
    """
    return {
        name: technique
        for name, technique in ALL_PFA_TECHNIQUES.items()
        if technique.category == category
    }


def suggest_pfa_technique(situation: str) -> List[str]:
    """
    Предложить технику PFA на основе ситуации.

    Args:
        situation: Описание ситуации пользователя

    Returns:
        List[str]: Список имён подходящих техник

    Example:
        >>> suggest_pfa_technique("паника, не могу дышать")
        ['box_breathing', '5_4_3_2_1']
    """
    situation_lower = situation.lower()

    # Маркеры для разных техник
    panic_markers = ["паника", "не могу дышать", "задыхаюсь", "сердце бьётся"]
    dissociation_markers = ["не в теле", "всё плывёт", "нереально", "как во сне"]
    crisis_markers = ["умереть", "покончить", "суицид", "не хочу жить"]

    suggestions = []

    # Кризис — немедленно к службам
    if any(marker in situation_lower for marker in crisis_markers):
        suggestions.append("crisis_services")

    # Паника — дыхание
    if any(marker in situation_lower for marker in panic_markers):
        suggestions.extend(["box_breathing", "5_4_3_2_1"])

    # Диссоциация — заземление
    if any(marker in situation_lower for marker in dissociation_markers):
        suggestions.extend(["5_4_3_2_1", "physical_grounding"])

    # Если ничего не подошло — базовое заземление
    if not suggestions:
        suggestions.append("5_4_3_2_1")

    return suggestions


