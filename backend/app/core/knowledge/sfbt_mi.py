# ФУНКЦИИ ДЛЯ ИСПОЛЬЗОВАНИЯ В ПРОМПТАХ
# ============================================================================

def get_sfbt_technique(name: str) -> SFBTTechnique:
    """
    Получить SFBT технику по имени.

    Args:
        name: Имя техники

    Returns:
        SFBTTechnique: SFBT техника

    Example:
        >>> technique = get_sfbt_technique("miracle_question")
        >>> print(technique.example_question)
    """
    return ALL_SFBT_TECHNIQUES.get(name)


def get_mi_technique(name: str) -> MITechnique:
    """
    Получить MI технику по имени.

    Args:
        name: Имя техники

    Returns:
        MITechnique: MI техника

    Example:
        >>> technique = get_mi_technique("open_ended")
        >>> print(technique.examples)
    """
    return ALL_MI_TECHNIQUES.get(name)


def get_mi_by_category(category: str) -> Dict[str, MITechnique]:
    """
    Получить все MI техники по категории OARS.

    Args:
        category: Категория (open, affirm, reflect, summarize)

    Returns:
        Dict[str, MITechnique]: Словарь техник

    Example:
        >>> reflections = get_mi_by_category("reflect")
        >>> print(list(reflections.keys()))
        ['simple', 'complex', 'amplified']
    """
    return {
        name: technique
        for name, technique in ALL_MI_TECHNIQUES.items()
        if technique.category == category
    }


def suggest_sfbt_technique(situation: str) -> List[str]:
    """
    Предложить SFBT технику на основе ситуации.

    Args:
        situation: Описание ситуации пользователя

    Returns:
        List[str]: Список имён подходящих техник

    Example:
        >>> suggest_sfbt_technique("не знаю, что делать, всё плохо")
        ['miracle_question', 'scaling']
    """
    situation_lower = situation.lower()

    # Маркеры для разных техник
    stuck_markers = ["не знаю", "застрял", "тупик", "нет выхода"]
    always_never_markers = ["всегда", "никогда", "постоянно"]
    ambivalent_markers = ["не знаю, стоит ли", "с одной стороны", "но с другой"]
    coping_markers = ["справляюсь", "продолжаю", "всё равно делаю"]

    suggestions = []

    # Застрял — вопрос о чуде
    if any(marker in situation_lower for marker in stuck_markers):
        suggestions.append("miracle_question")

    # Всегда/никогда — исключения
    if any(marker in situation_lower for marker in always_never_markers):
        suggestions.append("exceptions")

    # Амбивалентность — шкалирование
    if any(marker in situation_lower for marker in ambivalent_markers):
        suggestions.append("scaling")

    # Справляется — вопрос о совладании
    if any(marker in situation_lower for marker in coping_markers):
        suggestions.append("coping_question")

    # Если ничего не подошло — шкалирование (универсальная техника)
    if not suggestions:
        suggestions.append("scaling")

    return suggestions


