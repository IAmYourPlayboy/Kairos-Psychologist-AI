# ЭКСПОРТ ДЛЯ ИСПОЛЬЗОВАНИЯ В ПРОМПТАХ
# ============================================================================

def get_distortion_by_keywords(text: str) -> List[str]:
    """
    Определяет возможные когнитивные искажения по ключевым словам.

    Args:
        text: Текст сообщения пользователя

    Returns:
        List[str]: Список названий искажений
    """
    text_lower = text.lower()
    detected = []

    # Чёрно-белое мышление
    if any(word in text_lower for word in ["всегда", "никогда", "все", "никто", "либо", "или"]):
        detected.append("all_or_nothing")

    # Катастрофизация
    if any(word in text_lower for word in ["катастрофа", "ужасно", "кошмар", "конец", "всё пропало"]):
        detected.append("catastrophizing")

    # Долженствование
    if any(word in text_lower for word in ["должен", "обязан", "надо", "необходимо"]):
        detected.append("should_statements")

    # Навешивание ярлыков
    if any(word in text_lower for word in ["я неудачник", "я тупой", "я плохой", "я ничтожество"]):
        detected.append("labeling")

    return detected


