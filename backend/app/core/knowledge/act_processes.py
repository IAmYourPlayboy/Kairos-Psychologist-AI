def suggest_act_process(situation: str) -> List[str]:
    """
    Предлагает ACT процессы на основе ситуации.

    Args:
        situation: Описание ситуации пользователя

    Returns:
        List[str]: Список названий подходящих процессов
    """
    situation_lower = situation.lower()
    suggestions = []

    # Борьба с мыслями/эмоциями → Принятие
    if any(word in situation_lower for word in ["не могу", "борюсь", "пытаюсь избавиться", "не должен чувствовать"]):
        suggestions.append("acceptance")

    # Слияние с мыслями → Дефузия
    if any(word in situation_lower for word in ["я неудачник", "я плохой", "я ничтожество", "я не могу"]):
        suggestions.append("cognitive_defusion")

    # Застрял в прошлом/будущем → Настоящий момент
    if any(word in situation_lower for word in ["переживаю", "думаю о", "не могу забыть", "боюсь что будет"]):
        suggestions.append("present_moment")

    # Потеря смысла → Ценности
    if any(word in situation_lower for word in ["зачем", "смысл", "не знаю зачем", "бессмысленно"]):
        suggestions.append("values")

    # Избегание → Целенаправленное действие
    if any(word in situation_lower for word in ["боюсь", "не могу заставить", "откладываю", "избегаю"]):
        suggestions.append("committed_action")

    return suggestions


def get_metaphor_for_situation(situation: str) -> str:
    """
    Возвращает подходящую метафору для ситуации.

    Args:
        situation: Описание ситуации пользователя

    Returns:
        str: Ключ метафоры из ACT_METAPHORS
    """
    situation_lower = situation.lower()

    if any(word in situation_lower for word in ["борюсь", "пытаюсь избавиться"]):
        return "quicksand"
    elif any(word in situation_lower for word in ["мысли не отпускают", "навязчивые"]):
        return "passengers_bus"
    elif any(word in situation_lower for word in ["я — это моя тревога", "я депрессивный"]):
        return "sky_clouds"
    elif any(word in situation_lower for word in ["не знаю куда идти", "потерял направление"]):
        return "compass"
    else:
        return "garden"


