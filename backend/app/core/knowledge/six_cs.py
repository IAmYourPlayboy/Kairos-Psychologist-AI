# ФУНКЦИИ ДЛЯ РАБОТЫ С SIX C's
# ============================================================================

def get_component(component_name: str) -> SixCComponent:
    """Получить компонент SIX C's по имени."""
    return SIX_CS_COMPONENTS.get(component_name)


def get_next_component(current_component: str) -> str:
    """Получить следующий компонент в последовательности."""
    for i, step in enumerate(SIX_CS_SEQUENCE):
        if step["component"] == current_component:
            if i + 1 < len(SIX_CS_SEQUENCE):
                return SIX_CS_SEQUENCE[i + 1]["component"]
    return None


def suggest_six_cs_technique(
    distress_score: float,
    has_action_verbs: bool,
    is_stuck: bool
) -> str:
    """
    Предложить компонент SIX C's на основе контекста.

    Args:
        distress_score: Уровень дистресса (0.0-1.0)
        has_action_verbs: Есть ли глаголы действия в речи
        is_stuck: Застрял ли человек (не видит выхода)

    Returns:
        Название компонента для использования
    """
    # Высокий дистресс → сначала Calmness
    if distress_score > 0.8:
        return "calmness"

    # Глаголы действия + застрял → Challenge
    if has_action_verbs and is_stuck:
        return "challenge"

    # Нет глаголов действия → Control (дать выбор)
    if not has_action_verbs:
        return "control"

    # По умолчанию начинаем с Challenge
    return "challenge"


def format_six_cs_for_prompt(component_name: str) -> str:
    """
    Форматировать компонент SIX C's для включения в промпт.

    Returns:
        Строка с инструкциями для LLM
    """
    component = get_component(component_name)
    if not component:
        return ""

    return f"""
=== SIX C's: {component.russian_name} ({component.name}) ===

ЦЕЛЬ: {component.goal}

ТЕХНИКИ:
{chr(10).join(f"- {tech}" for tech in component.techniques)}

ПРИМЕРЫ ВОПРОСОВ:
{chr(10).join(f"- {q}" for q in component.example_questions)}

КОГДА ИСПОЛЬЗОВАТЬ: {component.when_to_use}

⚠️ НЕ ДЕЛАТЬ:
{chr(10).join(f"- {contra}" for contra in component.contraindications)}
"""
