CBT_TRIGGERS: str = """\
КАК РАСПОЗНАТЬ КОГНИТИВНЫЙ ТРИГГЕР (CBT-подход):

Триггер CBT-типа — это ситуация, активирующая стойкое когнитивное искажение.
В речи проявляется через повторяющиеся паттерны мышления вокруг одной темы:

- Катастрофизация: «всё развалится», «я не справлюсь никогда», «это конец».
- Чёрно-белое мышление: «или идеально, или провал», «либо все, либо никто».
- Долженствование: «я должен», «они обязаны», «надо было».
- Чтение мыслей: «они думают что я...», «он точно считает меня...».
- Персонализация: «это всё из-за меня», «я виноват во всём».
- Сверхобобщение: «всегда», «никогда», «никто меня не», «все они одинаковые».
- Ментальный фильтр: фокус только на негативе, игнорирование позитива.
- Навешивание ярлыков: «я неудачник», «я тупой», «он идиот».

ВАЖНО: триггером считается УСТОЙЧИВЫЙ паттерн вокруг одной темы.
Если катастрофизация появляется только в разговоре про работу
(«если меня уволят, моя жизнь развалится») — это триггер для папки
``triggers/cognitive`` с темой «работа».
"""

DBT_TRIGGERS: str = """\
КАК РАСПОЗНАТЬ ЭМОЦИОНАЛЬНЫЙ ТРИГГЕР (DBT-подход):

Триггер DBT-типа — это активатор эмоциональной дисрегуляции
(когда эмоция «затопляет» и человек теряет способность действовать
разумно). В речи проявляется через:

- Резкие эмоциональные перепады в одном сообщении.
- «Я взрываюсь», «не могу остановиться», «накрывает», «срываюсь».
- Импульсивные действия в ответ на эмоцию (срыв, причинение себе
  вреда, уход в употребление, разрыв отношений в моменте).
- Потеря способности рефлексировать в моменте: пользователь не может
  ответить «что ты чувствуешь?», только описывает действия.
- Физические симптомы вместе с эмоцией: трясёт, душит, ком в горле.

Триггер DBT-типа идёт в папку ``triggers/emotional_dysregulation``.

ОТЛИЧИЕ ОТ РАЗОВОЙ РЕАКЦИИ:
- Реакция в моменте («сегодня грустно из-за погоды») — НЕ факт-триггер,
  это просто запись настроения дня.
- Устойчивая зацепка («меня всегда выбивает разговор с мамой»,
  «после работы я взрываюсь на близких») — это факт-триггер.

Различай эти случаи в извлекаемых фактах: триггер должен быть назван
ХОТЯ БЫ ДВАЖДЫ в разных местах истории, либо явно сформулирован
пользователем как паттерн («я всегда так на это реагирую»).
"""


