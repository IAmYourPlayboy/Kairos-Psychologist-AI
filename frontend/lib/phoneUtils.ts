/**
 * Утилиты работы с телефонными номерами для tel: ссылок и aria-label.
 *
 * Разделители (`-`, пробелы, `(`, `)`) убираются для href="tel:..." —
 * dialer ожидает только цифры и `+`.
 *
 * Для aria-label, наоборот, добавляем пробелы между цифрами,
 * чтобы screen reader произносил их по одной, а не как одно число.
 */

/**
 * Преобразует "8-800-333-44-34" → "88003334434" для использования в `href="tel:..."`.
 * Сохраняет `+` (для международных номеров).
 */
export function toTelHref(phone: string): string {
  return phone.replace(/[^\d+]/g, "");
}

/**
 * Преобразует "8-800-333-44-34" → "8 8 0 0 3 3 3 4 4 3 4" — это нужно для
 * aria-label, чтобы screen reader произносил цифры по одной.
 *
 * Используется в карточках контактов: aria-label="Позвонить МЧС: 8 8 0 0...".
 */
export function spellPhoneForAria(phone: string): string {
  return phone.split("").join(" ");
}
